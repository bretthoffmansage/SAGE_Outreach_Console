function evaluateThursday(
  c: Campaign,
  checkDate: string,
  tasks: HeartbeatTaskSpec[],
  missing: string[],
  risk: string[],
  approvals: ApprovalSnapshot[],
) {
  const eb = nz(c.emailBriefStatus);
  const cb = nz(c.creativeBriefStatus);
  if (eb === "drafted" || eb === "ready_to_send") risk.push("Emailmarketing.com handoff may not be confirmed.");
  if (cb === "drafted" || cb === "ready_to_send") risk.push("Creative handoff may not be confirmed.");
  if (nz(c.shortsStatus) === "requested") risk.push("Shorts/reels status needs follow-up.");
  const sc = nz(c.socialCopyStatus);
  if (!sc || NOT_STARTED.has(sc) || sc === "draft_needed") missing.push("Social copy is not ready for rollout.");
  const rc = nz(c.rolloutCadenceStatus);
  if (!rc || NOT_STARTED.has(rc)) missing.push("Rollout cadence is missing.");
  const bl = c.blockers ?? [];
  if (bl.length) risk.push("Launch packet has listed blockers.");

  if (!["sent", "confirmed"].includes(eb)) {
    pushTask(tasks, {
      dedupeKey: taskDedupe(c.id, checkDate, "thursday", "confirm_email_handoff"),
      title: "Confirm Emailmarketing.com has the brief and CTA link",
      context: "Mid-week follow-up reduces Friday surprises for the email sequence.",
      category: "Follow-up",
      taskCategory: "follow_up",
      ownerLabel: "Kelsey",
      ownerType: "internal_user",
      externalDependency: "Emailmarketing.com",
      destinationRoute: `/campaigns/${c.id}#packet-email`,
      hbRisk: "medium",
    });
  }
  if (!["sent", "confirmed"].includes(cb)) {
    pushTask(tasks, {
      dedupeKey: taskDedupe(c.id, checkDate, "thursday", "confirm_creative_timeline"),
      title: "Confirm creative timeline",
      context: "Ensure the creative owner has acknowledged the brief and timeline for reels/shorts.",
      category: "Follow-up",
      taskCategory: "follow_up",
      ownerLabel: "Kelsey",
      ownerType: "internal_user",
      externalDependency: "Brandon",
      destinationRoute: `/campaigns/${c.id}#packet-creative`,
      hbRisk: "medium",
    });
  }
  if (!["drafted", "needs_review", "ready", "scheduled", "published"].includes(sc)) {
    pushTask(tasks, {
      dedupeKey: taskDedupe(c.id, checkDate, "thursday", "social_finalize"),
      title: "Finalize social captions and rollout cadence",
      context: "Supporting posts and cadence should be ready or in review before Friday readiness.",
      category: "Social",
      taskCategory: "social",
      ownerLabel: "Kelsey",
      ownerType: "internal_user",
      destinationRoute: `/campaigns/${c.id}#packet-social`,
      hbRisk: "medium",
    });
  }
  pushTask(tasks, {
    dedupeKey: taskDedupe(c.id, checkDate, "thursday", "review_blockers"),
    title: "Review blockers before Friday readiness",
    context: "Clear or escalate blockers so Friday sign-off reflects reality.",
    category: "Readiness",
    taskCategory: "readiness",
    ownerLabel: "Internal",
    ownerType: "internal_user",
    destinationRoute: `/campaigns/${c.id}#packet-blockers`,
    hbRisk: bl.length ? "high" : "medium",
  });

  const pend = pendingApprovalsForCampaign(approvals, c.id);
  const internalPending = pend.filter((p) => p.owner === "internal");
  if (internalPending.length) {
    pushTask(tasks, {
      dedupeKey: taskDedupe(c.id, checkDate, "thursday", "internal_approval_queue"),
      title: "Clear internal review items",
      context: "Internal approvals are still pending for this launch packet.",
      category: "Approval",
      taskCategory: "approval",
      ownerLabel: "Internal",
      ownerType: "internal_user",
      destinationRoute: "/reviews/internal",
      hbRisk: "medium",
    });
  }
}

function socialReadyFriday(c: Campaign) {
  const sc = nz(c.socialCopyStatus);
  return sc === "ready" || sc === "scheduled" || sc === "published";
}

function rolloutReadyFriday(c: Campaign) {
  const rc = nz(c.rolloutCadenceStatus);
  return rc === "approved" || rc === "scheduled" || rc === "active" || rc === "complete";
}

function shortsOkFriday(c: Campaign) {
  const s = nz(c.shortsStatus);
  return (
    ["ready", "scheduled", "published", "in_progress", "first_draft_received"].includes(s) ||
    (s === "revisions_needed" && hasText(c.relatedShortsNotes))
  );
}

function evaluateFriday(
  c: Campaign,
  checkDate: string,
  tasks: HeartbeatTaskSpec[],
  missing: string[],
  blockers: string[],
  completed: string[],
  approvals: ApprovalSnapshot[],
) {
  if (!hasSourceAsset(c)) missing.push("Source asset is not selected or entered.");
  if (!hasText(c.publishDate)) missing.push("Publish date is not set.");
  if (!hasText(c.youtubeScheduledUrl)) blockers.push("Launch cannot be marked ready because scheduled YouTube link is missing.");
  const eb = nz(c.emailBriefStatus);
  if (!["sent", "confirmed"].includes(eb)) blockers.push("Email handoff is not complete.");
  const cb = nz(c.creativeBriefStatus);
  if (!["sent", "confirmed"].includes(cb)) blockers.push("Creative handoff is not complete.");
  if (!socialReadyFriday(c)) missing.push("Social copy is not ready.");
  if (!rolloutReadyFriday(c)) missing.push("Rollout cadence is not ready.");
  if (!shortsOkFriday(c)) missing.push("Shorts/reels are not ready or clearly tracked.");
  const pend = pendingApprovalsForCampaign(approvals, c.id);
  if (pend.length) missing.push("Required approvals are still pending.");
  const bl = c.blockers ?? [];
  if (bl.length) blockers.push("Launch packet has unresolved blockers.");

  const hasGaps = missing.length > 0 || blockers.length > 0 || pend.length > 0;
  if (!hasGaps) {
    completed.push("Friday readiness: core checklist passed for scheduled link, handoffs, social, and approvals.");
    return;
  }

  pushTask(tasks, {
    dedupeKey: taskDedupe(c.id, checkDate, "friday", "readiness_review"),
    title: "Complete Friday launch readiness review",
    context: "Confirm that next week’s launch packet has the scheduled video link, email handoff, creative handoff, social rollout, and blockers accounted for.",
    category: "Readiness",
    taskCategory: "readiness",
    ownerLabel: "Internal",
    ownerType: "internal_user",
    destinationRoute: `/campaigns/${c.id}#packet-blockers`,
    hbRisk: "high",
  });
  if (bl.length || blockers.length) {
    pushTask(tasks, {
      dedupeKey: taskDedupe(c.id, checkDate, "friday", "resolve_blockers"),
      title: "Resolve launch packet blockers",
      context: "This launch packet has blockers that may prevent it from being ready for the next release window.",
      category: "Readiness",
      taskCategory: "readiness",
      ownerLabel: "Brett",
      ownerType: "internal_user",
      destinationRoute: `/campaigns/${c.id}#packet-blockers`,
      hbRisk: "blocked",
    });
  }
  pushTask(tasks, {
    dedupeKey: taskDedupe(c.id, checkDate, "friday", "confirm_ready_next_week"),
    title: "Confirm launch packet is ready for next week",
    context: "Friday sign-off: scheduled link, handoffs, social, and approvals are either complete or consciously accepted.",
    category: "Readiness",
    taskCategory: "readiness",
    ownerLabel: "Brett",
    ownerType: "internal_user",
    destinationRoute: `/campaigns/${c.id}#packet-overview`,
    hbRisk: "medium",
  });

  const bariItems = pend.filter((p) => p.owner === "bari");
  const blueItems = pend.filter((p) => p.owner === "blue");

  if (bariItems.length) {
    pushTask(tasks, {
      dedupeKey: taskDedupe(c.id, checkDate, "friday", "bari_founder_voice"),
      title: "Founder voice review (Bari)",
      context: "A founder-voice approval is pending or a blocker explicitly calls for Bari.",
      category: "Approval",
      taskCategory: "approval",
      ownerLabel: "Bari",
      ownerType: "reviewer",
      destinationRoute: "/reviews/bari",
      hbRisk: "high",
    });
  }
  if (blueItems.length) {
    pushTask(tasks, {
      dedupeKey: taskDedupe(c.id, checkDate, "friday", "blue_strategy"),
      title: "Strategy / claims review (Blue)",
      context: "A strategy or claims approval is pending or a blocker explicitly calls for Blue.",
      category: "Approval",
      taskCategory: "approval",
      ownerLabel: "Blue",
      ownerType: "reviewer",
      destinationRoute: "/reviews/blue",
      hbRisk: "high",
    });
  }
}

