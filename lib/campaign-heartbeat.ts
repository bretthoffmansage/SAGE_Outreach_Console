function deriveAggregateStatus(params: {
  missing: string[];
  risk: string[];
  blockers: string[];
  hasCampaign: boolean;
}): HeartbeatRunStatus {
  if (!params.hasCampaign) return "no_active_campaign";
  if (params.blockers.length) return "blocked";
  const highRisk = params.missing.some((m) => /scheduled YouTube|handoff is not complete|cannot be marked ready/i.test(m));
  if (highRisk && params.missing.length) return "at_risk";
  if (params.missing.length > 2) return "at_risk";
  if (params.missing.length || params.risk.length) return "needs_attention";
  return "clear";
}

function deriveCampaignHeartbeatStatus(status: HeartbeatRunStatus): HeartbeatEvaluationResult["campaignHeartbeatStatus"] {
  if (status === "no_active_campaign") return "no_check";
  if (status === "clear") return "clear";
  if (status === "blocked") return "blocked";
  if (status === "at_risk") return "at_risk";
  return "needs_attention";
}

function deriveReadinessAfter(c: Campaign, status: HeartbeatRunStatus, blockers: string[], missing: string[]): string | null {
  const cur = nz(c.readinessStatus);
  if (cur === "launched" || cur === "complete") return null;
  if (cur === "archived") return null;
  if (status === "no_active_campaign") return null;
  if (status === "blocked") {
    if (cur === "blocked") return null;
    return "blocked";
  }
  if (status === "at_risk" || status === "needs_attention") {
    if (cur === "blocked") return null;
    if (["ready", "launched", "complete"].includes(cur)) return null;
    return "at_risk";
  }
  if (status === "clear") {
    if (cur === "blocked") return null;
    if (blockers.length === 0 && missing.length === 0) return "ready";
  }
  return null;
}

function buildSummary(
  status: HeartbeatRunStatus,
  day: HeartbeatRuleDay,
  c: Campaign | null,
  missing: string[],
  blockers: string[],
) {
  const ck = checkpointLabel(day);
  if (status === "no_active_campaign") {
    return "No active launch packet found. Create or select the next Weekly Launch Packet so the heartbeat can track daily readiness.";
  }
  if (status === "clear") return `Launch prep is on track for ${ck}. The active launch packet (${c?.name}) has the expected pieces for this stage.`;
  if (status === "blocked")
    return `${ck}: this launch packet is blocked — ${blockers[0] ?? missing[0] ?? "review blockers and handoffs"}.`;
  if (status === "at_risk")
    return `${ck}: this launch packet is at risk — ${missing.slice(0, 2).join("; ") || "review missing fields"}.`;
  return `${ck}: this launch packet needs attention — ${missing[0] ?? "complete outstanding checklist items"}.`;
}

export function runHeartbeatEvaluation(input: HeartbeatEvaluationInput): HeartbeatEvaluationResult {
  const { campaigns, approvals, checkType, explicitCampaignId, checkDate, nowMs, productionAssets } = input;
  const weekday = calendarWeekdayLower(new Date(nowMs));
  const ruleDay = resolveRuleDay(weekday, checkType);
  const assetMap = buildProductionAssetMap(productionAssets);

  let primary: Campaign | null = null;
  if (explicitCampaignId) {
    primary = campaigns.find((c) => c.id === explicitCampaignId) ?? null;
    if (!primary) {
      const checkId = `heartbeat_${checkDate}_${Math.random().toString(36).slice(2, 10)}`;
      return {
        checkId,
        campaignId: explicitCampaignId,
        campaignName: "Not found",
        checkDate,
        weekday: calendarWeekdayLower(new Date(nowMs)),
        checkType,
        status: "no_active_campaign",
        currentCheckpoint: "Campaign detail check",
        summary: "Campaign Heartbeat could not find that launch packet in the current workspace.",
        completedItems: [],
        missingItems: ["Campaign record not found for this id."],
        riskItems: [],
        blockerItems: [],
        recommendedActions: ["Return to Campaigns and pick an existing Weekly Launch Packet."],
        taskSpecs: [],
        readinessStatusBefore: undefined,
        readinessStatusAfter: null,
        readinessReasons: [],
        campaignHeartbeatStatus: "no_check",
      };
    }
  } else {
    primary = selectActiveLaunchCampaign(campaigns, checkDate);
  }

  if (!primary) {
    return noActiveCampaignEvaluation(checkDate, checkType);
  }

  const additional = countAdditionalActive(campaigns, primary, checkDate);
  const additionalNote =
    additional > 0
      ? `${additional} additional upcoming launch packet(s) exist; only the nearest publish date is checked in this run.`
      : undefined;

  const missing: string[] = [];
  const risk: string[] = [];
  const blockers: string[] = [];
  const completed: string[] = [];
  const tasks: HeartbeatTaskSpec[] = [];

  if (ruleDay === "weekend") {
    const readiness = nz(primary.readinessStatus);
    const bl = primary.blockers ?? [];
    if (readiness === "blocked" || bl.length > 0) {
      risk.push("Launch packet may still need attention before Monday.");
    }
    evaluateWeekend(primary, checkDate, tasks, completed);
  } else if (ruleDay === "monday") {
    evaluateMonday(primary, checkDate, tasks, missing, completed);
  } else if (ruleDay === "tuesday") {
    evaluateTuesday(primary, checkDate, tasks, missing, completed);
  } else if (ruleDay === "wednesday") {
    evaluateWednesday(primary, checkDate, tasks, missing, completed);
  } else if (ruleDay === "thursday") {
    evaluateThursday(primary, checkDate, tasks, missing, risk, approvals);
  } else if (ruleDay === "friday") {
    evaluateFriday(primary, checkDate, tasks, missing, blockers, completed, approvals);
  }

  applyProductionBridgeLayer(primary, assetMap, risk, blockers, tasks, checkDate, nowMs);

  const status = deriveAggregateStatus({ missing, risk, blockers, hasCampaign: true });
  const readinessBefore = nz(primary.readinessStatus);
  const readinessAfter = deriveReadinessAfter(primary, status, blockers, missing);
  const readinessReasons = [...missing, ...blockers].slice(0, 8);
  const summary = buildSummary(status, ruleDay, primary, missing, blockers);
  const checkId = `heartbeat_${checkDate}_${Math.random().toString(36).slice(2, 10)}`;

  const recommendedActions = [
    ...tasks.slice(0, 6).map((t) => t.title),
    ...(additionalNote ? [additionalNote] : []),
  ];

  return {
    checkId,
    campaignId: primary.id,
    campaignName: primary.name,
    checkDate,
    weekday,
    checkType,
    status,
    currentCheckpoint: checkpointLabel(ruleDay),
    summary: additionalNote ? `${summary} ${additionalNote}` : summary,
    completedItems: completed,
    missingItems: missing,
    riskItems: risk,
    blockerItems: blockers,
    recommendedActions,
    taskSpecs: tasks,
    readinessStatusBefore: readinessBefore || undefined,
    readinessStatusAfter: readinessAfter,
    readinessReasons,
    campaignHeartbeatStatus: deriveCampaignHeartbeatStatus(status),
    additionalLaunchPacketsNote: additionalNote,
  };
}

